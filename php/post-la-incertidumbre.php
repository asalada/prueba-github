        <p>
            Se me hace difícil explicar el porqué. El por qué no me entiendo. El por qué sigo siendo una incógnita después de tantos años. El no ser capaz de adelantarme, el caer en errores que creía superados y en encontrar momentos de lucidez en lugares que creía perdidos. Es incomprensible, porque si con alguien he pasado tiempo ha sido conmigo mismo.
        </p>
        
        <p>
            Es una gran incógnita, pero tiene que ser así. ¿Qué gracia tendría saber exactamente de qué eres capaz y de qué no? Equivocarse, fracasar, es parte del viaje. Es parte del éxito, del camino a la felicidad. Y perderla, y sentir la necesidad de volverla a recuperar.
        </p>
        
        <p>
            Los estados son pasajeros. Nadie es capaz de evitar los días malos, esos días en que por más que lo pretendas el nubarrón forma parte de tu cabeza. Días que parecen definitivos, en los que te miras al espejo y no ves nada más que lo que ya conoces. Sin embargo otros te miras al espejo y ves lo que puedes llegar a ser, y te emociona el camino que te llevará a descubrirlo.
        </p>
        
        <p>
            Tiene que ser así, de otra forma sería sencillamente aburrido.
        </p>
        
        <p>
            Qué estúpido es estar seguro de uno mismo. Qué falso. Aquella vez que pensabas que lo sabías todo, y sin sentido o aviso, todo fue diferente. Para bien o para mal. Qué bonito cuando es para bien, qué jodido cuando es para mal. Pero entre ese riesgo o la seguridad de saber lo que va a pasar, ¿por qué alguien querría quedarse con un futuro escrito? Esa sensación de que por mucho que hayas hecho, todo está por escribir y puede ser diferente. Formas parte de esto, tanto como el que ya tiene mucho. O el que tiene menos. Y para subir o para bajar, te necesitas.
        </p>
        
